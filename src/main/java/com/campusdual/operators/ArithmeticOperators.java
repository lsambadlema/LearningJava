package com.campusdual.operators;

public class ArithmeticOperators {
    public static void main(String[] args) {

        int value = 5;                 // 5
        value = value + 3;              //8
        value = 2 - value;              //-6
        value += 3;                     //-3
        value -= 1;                     //-4
        value *= 4;                     //-16
        value = (value - 2) / 2;        //-9
        int newValue = 3;               //3
        int myValue = 2;                //2
        value = newValue + myValue;     //5
        final int CONST = 2;            //2
        value = CONST;                  //2
        value +=3;                      //5
        value = value % 2;              //1
        value++;                        //2
        value--;                        //1
        System.out.println(value);      //1


    }
}

